    private void loadTable() {
        dtm = (DefaultTableModel) tb_danhmuc.getModel();
        dtm.setRowCount(0);
        int stt = 0;
        for (DanhMuc danhMuc : this.danhMucService.read()) {
            Object[] rowdata = {
                stt++,
                danhMuc.getMa(),
                danhMuc.getTen(),
                danhMuc.getTrangThai()
            };
            dtm.addRow(rowdata);
        }
    }

